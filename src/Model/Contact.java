package Model;

/**
 *
 * @author shalomdaniel
 */
public class Contact {

    long phone_number;
    String email;
    
    public Contact() {
        
    }
    
    public long getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(long phone_number) {
        this.phone_number = phone_number;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
